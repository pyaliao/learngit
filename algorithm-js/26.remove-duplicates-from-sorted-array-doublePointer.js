// 说明:
// 为什么返回数值是整数，但输出的答案是数组呢 ?
// 请注意，输入数组是以「引用」方式传递的，
// 这意味着在函数里修改输入数组对于调用者是可见的。

// 你可以想象内部操作如下:
// nums 是以“引用”方式传递的。也就是说，不对实参做任何拷贝
// int len = removeDuplicates(nums);
// 在函数里修改输入数组对于调用者是可见的。
// 根据你的函数返回的长度, 它会打印出数组中 该长度范围内 的所有元素。
